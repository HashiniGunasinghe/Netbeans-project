   public void addfieldofficerLoad()
   {
       int c;
        try {
            pst = con.prepareStatement("SELECT * FROM field_register");
            rs = pst.executeQuery();
           
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            d = (DefaultTableModel)tbl_fieldofficer.getModel();
         
            d.setRowCount(0);
            
           
//** While loop for load the data
            while(rs.next())
            {
              Vector v2 = new Vector();
              for(int i=1; i<=c; i++)
              {
              
              v2.add(rs.getString("uorn"));
              v2.add(rs.getString("field_fname"));
              v2.add(rs.getString("field_lname"));
              v2.add(rs.getString("field_address"));
              v2.add(rs.getString("field_email"));
              v2.add(rs.getString("field_district"));
              v2.add(rs.getString("field_telno"));
            
              
                                      
              }
             
             d.addRow(v2);
             
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(fertilizer_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    


