        public void Uorn()
   {
       try
       {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT MAX(uorn) FROM field_register");
            rs.next();
            rs.getString("MAX(uorn)");
           
            if(rs.getString("MAX(uorn)")==null)
            {
                txtuorn.setText("UORN00001");
            }
            else
            {
                long uorn = Long.parseLong(rs.getString("MAX(uorn)").substring(4,rs.getString("MAX(uorn)").length()));
                uorn++;
                txtuorn.setText("UORN" + String.format("%05d", uorn));
            }
        }
       
     catch (SQLException ex) {
            Logger.getLogger(Field_officer_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
   }

        
        
        
        
        
        
