    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        
        
        String fer_id=txtfertilizerid.getText();
        String name=txtname.getText();
        String fer_name=txtfertilizername.getText();
        String quota=txtquota.getText();
        String refill_date=txtfer_refilldate.getText();
        String m_date=txtfe_manudate.getText();
        String exp_date=txtfe_expdate.getText();
        
        
        
        
        
         
        if (fer_id.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Fertilizer ID");
            }
        else if (name.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Inventory Name");
            }
        else if (fer_name.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Fertiliizer name");
            }
        else if (quota.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Quota");
            }
          
        else if (refill_date.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Refill Date");
            }
        else if (m_date.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Manufacture Date");
            }
        else if (exp_date.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Expire Date");
            }
       
        else
        {
        
        
        
        
        String sql=" UPDATE fertilizer SET fertilizer_id=?,inventory_name=?, fertilizer_name=? , quota=? , fer_refilldate=? , fer_manudate=?, fer_expdate=? WHERE fertilizer_id='"+fer_id+"'";
        
        
        
        
     
        try
        {
            pst=con.prepareStatement(sql);
           
            pst.setString(1, txtfertilizerid.getText());
            pst.setString(2, txtname.getText());
            pst.setString(3, txtfertilizername.getText());
            pst.setString(4, txtquota.getText());
            pst.setString(5,txtfer_refilldate.getText());
            pst.setString(6,txtfe_manudate.getText());
            pst.setString(7,txtfe_expdate.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Values have been updated successfully");
            
            
            
            
            
           //clear added data
            txtname.setText("");
            txtfertilizerid.setText("");
            txtfertilizername.setText("");
            txtquota.setText("");
            txtfer_refilldate.setText("");
            txtfe_manudate.setText("");
            txtfe_expdate.setText("");
            Fertilizerid();
            addfertilizerLoad();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btnupdateActionPerformed

