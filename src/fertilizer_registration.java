    //auto gen fertilizer Id    
        public void Fertilizerid()
   {
       try
       {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT MAX(fertilizer_id) FROM fertilizer");
            rs.next();
            rs.getString("MAX(fertilizer_id)");
           
            if(rs.getString("MAX(fertilizer_id)")==null)
            {
                txtfertilizerid.setText("FIR00001");
            }
            else
            {
                long fertilizerid = Long.parseLong(rs.getString("MAX(fertilizer_id)").substring(3,rs.getString("MAX(fertilizer_id)").length()));
                fertilizerid++;
                txtfertilizerid.setText("FIR" + String.format("%05d", fertilizerid));
            }
        }
       
     catch (SQLException ex) {
            Logger.getLogger(fertilizer_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
   }
        
        
        
        
        
        
        //Adding Item to Jtable
   public void addfertilizerLoad()
   {
       int c;
        try {
            pst = con.prepareStatement("SELECT * FROM fertilizer");
            rs = pst.executeQuery();
           
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            d = (DefaultTableModel)tbl_fertilizer.getModel();
         
            d.setRowCount(0);
            
           
//** While loop for load the data
            while(rs.next())
            {
              Vector v2 = new Vector();
              for(int i=1; i<=c; i++)
              {
              
              v2.add(rs.getString("fertilizer_id"));
              v2.add(rs.getString("inventory_name"));
              v2.add(rs.getString("fertilizer_name"));
              v2.add(rs.getString("quota"));
              v2.add(rs.getString("fer_refilldate"));
              v2.add(rs.getString("fer_manudate"));
              v2.add(rs.getString("fer_expdate"));
                                      
              }
             
             d.addRow(v2);
             
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(fertilizer_registration.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    

