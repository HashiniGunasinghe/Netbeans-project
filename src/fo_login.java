    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        String uorn = txtuorn.getText();
        String pass = txtpssword.getText();

        {

            try {
                String query = "SELECT * FROM `field_register` WHERE `uorn` =? AND `field_password` =?";

                pst=con.prepareStatement(query);
                pst.setString(1, uorn);
                pst.setString(2, pass);
                rs = pst.executeQuery();

                if(rs.next())
                {
                    fo_login log= new fo_login();
                    fo_homepage As = new fo_homepage();
                    log.dispose();
                    As.setVisible(true);
                    this.dispose();

                }
                else{
                    JOptionPane.showMessageDialog(null, "Incorrect UORN Or Password", "Login Failed", 2);
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Give details");

            }
        }
    }//GEN-LAST:event_btnloginActionPerformed

