        public void Allocation_id()
   {
       try
       {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT MAX(allocation_id) FROM district_allocation");
            rs.next();
            rs.getString("MAX(allocation_id)");
           
            if(rs.getString("MAX(allocation_id)")==null)
            {
                txtid1.setText("FID00001");
            }
            else
            {
                long allocationid = Long.parseLong(rs.getString("MAX(allocation_id)").substring(3,rs.getString("MAX(allocation_id)").length()));
               allocationid++;
                txtid1.setText("FID" + String.format("%05d", allocationid));
            }
        }
       
     catch (SQLException ex) {
            Logger.getLogger(district_allocation.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
       
   }

        
