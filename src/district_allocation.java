   public void addallocatingdistrict()
   {
       int c;
        try {
            pst = con.prepareStatement("SELECT * FROM fertilizer");
            rs = pst.executeQuery();
           
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            d = (DefaultTableModel)tblfer_alo.getModel();
         
            d.setRowCount(0);
            
           
//** While loop for load the data
            while(rs.next())
            {
              Vector v2 = new Vector();
              for(int i=1; i<=c; i++)
              {
              
              v2.add(rs.getString("fertilizer_id"));
              v2.add(rs.getString("fertilizer_name"));
              v2.add(rs.getString("fer_expdate"));
              v2.add(rs.getString("quota"));
                                  
              }
             
             d.addRow(v2);
             
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(district_allocation.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
